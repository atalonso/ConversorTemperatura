def celsius_para_kelvin(temp_celsius):
 """Função que converte a temperatura de Celsius para Kelvin"""
 """ Formula: C+273.15 """
 temp_kelvin = temp_celsius + 273.15
 return temp_kelvin

def celsius_para_fahrenheit(temp_celsius):
 """Função que converte a temperatura de Celsius para Fahrenheit """
 """ Formula: (temp_celsius*1,8)+32 """
 temp_fahrenheit = (temp_celsius*1.8)+32
 return temp_fahrenheit

